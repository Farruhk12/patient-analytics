/**
 * Получить норму для показателя с учётом пола И категории.
 * Категория обязательна для безопасности: показатель с тем же названием
 * в качественной категории (моча/мазок/кал) не должен матчиться на норму крови.
 *
 * @param {string} indicatorName — название показателя
 * @param {string} gender — "Муж" или "Жен"
 * @param {string} [category] — категория показателя
 * @returns {number[]|null} — [lower, upper] или null
 */
function getNorm(indicatorName, gender, category) {
  if (category && QUALITATIVE_CATEGORIES[category]) return null;
  var entry = NORMS[indicatorName];
  if (!entry) return null;
  var g = gender === 'Жен' ? 'Жен' : 'Муж';
  var range = entry[g];
  if (!range || range.length !== 2) return null;
  return range;
}

/**
 * Мягкий разбор медицинского значения из «грязных» данных.
 * Понимает:
 *   - числа с запятой-разделителем ("13,5" → 13.5);
 *   - разделители тысяч-пробелы ("1 200" → 1200);
 *   - научную запись с русской «х»/«*» ("1,64х10^4" → 16400);
 *   - диапазоны ("0-1", "5–7" → среднее 0.5 / 6, kind:"range");
 *   - "<0.5" / ">100" / "≤" / "≥" (kind:"lt"/"gt", num = граница);
 *   - титры "1:40" — НЕ число (kind:"titer");
 *   - качественные ("отр", "положит", "*", "единич") — kind:"qualitative".
 *
 * @param {*} raw
 * @returns {{num: (number|null), kind: string, text: string}}
 */
function parseMedicalValue(raw) {
  if (raw === null || raw === undefined || raw === '') {
    return { num: null, kind: 'empty', text: '' };
  }
  if (typeof raw === 'number') {
    return { num: isNaN(raw) ? null : raw, kind: 'number', text: String(raw) };
  }

  var s = String(raw).trim();
  if (!s) return { num: null, kind: 'empty', text: '' };

  var text = s;
  // Нормализуем: неразрывные пробелы, запятая-как-десятичный разделитель
  var t = s.replace(/\u00a0/g, ' ').trim();

  // Научная запись: "1,64х10^4", "3.5*10^3", "2e4"
  var sci = t.replace(/\s/g, '').replace(',', '.').replace(/[х*]/gi, 'x');
  var mSci = sci.match(/^([+-]?\d*\.?\d+)x10\^?([+-]?\d+)$/i);
  if (mSci) {
    var base = parseFloat(mSci[1]);
    var exp = parseInt(mSci[2], 10);
    if (!isNaN(base) && !isNaN(exp)) {
      return { num: base * Math.pow(10, exp), kind: 'number', text: text };
    }
  }

  // Титр "1:40" — не приводим к числу (иначе исказит классификацию)
  if (/^\d+\s*:\s*\d+$/.test(t)) {
    return { num: null, kind: 'titer', text: text };
  }

  // "<0.5", "≤ 0,5", ">100", "≥ 5"
  var mCmp = t.replace(',', '.').match(/^([<>≤≥])\s*([+-]?\d*\.?\d+)$/);
  if (mCmp) {
    var b = parseFloat(mCmp[2]);
    if (!isNaN(b)) {
      var kind = (mCmp[1] === '<' || mCmp[1] === '≤') ? 'lt' : 'gt';
      return { num: b, kind: kind, text: text };
    }
  }

  // Диапазон "0-1", "5–7", "1,2 - 3,4" (тире/дефис/эн-дэш)
  var mRange = t.replace(/\s/g, '').replace(/,/g, '.')
    .match(/^([+-]?\d*\.?\d+)[-–—]([+-]?\d*\.?\d+)$/);
  if (mRange) {
    var a = parseFloat(mRange[1]);
    var c = parseFloat(mRange[2]);
    if (!isNaN(a) && !isNaN(c)) {
      return { num: (a + c) / 2, kind: 'range', text: text };
    }
  }

  // Просто число (с запятой/пробелами-тысячами)
  var cleaned = t.replace(/\s+/g, '').replace(',', '.');
  if (/^[+-]?\d*\.?\d+$/.test(cleaned)) {
    var n = parseFloat(cleaned);
    if (!isNaN(n)) return { num: n, kind: 'number', text: text };
  }

  // Всё остальное — качественное значение
  return { num: null, kind: 'qualitative', text: text };
}

/**
 * Числовое значение показателя (или NaN). Единая точка входа для расчётов.
 * @param {*} raw
 * @returns {number} — число или NaN
 */
function numericValue(raw) {
  var p = parseMedicalValue(raw);
  return p.num === null ? NaN : p.num;
}

/**
 * Классифицировать значение относительно нормы.
 * @returns {string} — "normal", "high", "low" или "neutral"
 */
function classifyValue(value, low, high) {
  var num = numericValue(value);
  if (isNaN(num)) return 'neutral';
  if (num < low) return 'low';
  if (num > high) return 'high';
  return 'normal';
}

/**
 * Степень отклонения от нормы по относительному выходу за границу.
 * @returns {'normal'|'mild'|'moderate'|'severe'}
 */
function deviationSeverity(value, low, high) {
  var num = numericValue(value);
  if (isNaN(num) || low == null || high == null) return 'normal';
  var cls = classifyValue(num, low, high);
  if (cls === 'normal' || cls === 'neutral') return 'normal';
  var ref = cls === 'high' ? high : low;
  if (!ref && ref !== 0) return 'moderate';
  var pct = cls === 'high'
    ? (num - high) / Math.abs(ref || 1)
    : (low - num) / Math.abs(ref || 1);
  if (pct >= 0.5) return 'severe';
  if (pct >= 0.2) return 'moderate';
  return 'mild';
}

/**
 * Проверка правдоподобности значения — ловит опечатки ввода:
 * сдвиг запятой (135 вместо 13.5), лишние нули, «космические» цифры,
 * серийные номера дат (напр. 45689), перепутанные точка/запятая.
 *
 * Значение считается подозрительным, если оно на порядок(и) выходит за
 * физиологически возможный диапазон вокруг нормы.
 *
 * @param {number} num — уже разобранное число
 * @param {number[]} norm — [low, high]
 * @returns {{suspicious: boolean, reason: string}}
 */
function checkPlausibility(num, norm) {
  if (typeof num !== 'number' || isNaN(num) || !norm) {
    return { suspicious: false, reason: '' };
  }
  var low = norm[0], high = norm[1];

  // Отрицательное значение у показателя с неотрицательной нормой
  if (num < 0 && low >= 0) {
    return { suspicious: true, reason: 'отрицательное значение — проверьте ввод' };
  }

  // Сдвиг десятичной запятой (самый частый тип опечатки).
  // Флагуем ТОЛЬКО значения, далеко выходящие за норму (в ~4+ раза),
  // и лишь если деление/умножение на 10/100 возвращает их в норму.
  // Пример: гемоглобин 1350 → /10 = 135 (норма 130–170) → опечатка.
  // Умеренно повышенные значения (напр. B12 2000 при норме 187–883) НЕ трогаем —
  // это возможная реальная патология, а не опечатка.
  if (high > 0 && num > high * 4) {
    var downs = [0.1, 0.01];
    for (var i = 0; i < downs.length; i++) {
      var s = num * downs[i];
      if (s >= low && s <= high) {
        return { suspicious: true, reason: 'похоже на смещение запятой (проверьте: ' + (Math.round(s * 1000) / 1000) + ')' };
      }
    }
  }
  if (low > 0 && num > 0 && num < low / 4) {
    var ups = [10, 100];
    for (var j = 0; j < ups.length; j++) {
      var s2 = num * ups[j];
      if (s2 >= low && s2 <= high) {
        return { suspicious: true, reason: 'похоже на смещение запятой (проверьте: ' + (Math.round(s2 * 1000) / 1000) + ')' };
      }
    }
  }

  // «Космическое» значение — на порядок(и) выше физиологически возможного
  // (напр. серийный номер даты 45689). Реальную патологию не скрываем —
  // это лишь дополнительная пометка «проверьте ввод».
  if (high > 0 && num > high * 20) {
    return { suspicious: true, reason: 'аномально большое значение — проверьте ввод' };
  }
  if (low > 0 && num > 0 && num < low / 20) {
    return { suspicious: true, reason: 'аномально малое значение — проверьте ввод' };
  }

  return { suspicious: false, reason: '' };
}
