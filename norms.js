/**
 * Получить норму для показателя с учётом пола
 * @param {string} indicatorName — точное название показателя
 * @param {string} gender — "Муж" или "Жен"
 * @returns {number[]|null} — [lower, upper] или null
 */
function getNorm(indicatorName, gender) {
  var entry = NORMS[indicatorName];
  if (!entry) return null;
  var g = gender === 'Жен' ? 'Жен' : 'Муж';
  var range = entry[g];
  if (!range || range.length !== 2) return null;
  return range;
}

/**
 * Классифицировать значение относительно нормы
 * @param {*} value — значение (число или строка)
 * @param {number} low — нижняя граница нормы
 * @param {number} high — верхняя граница нормы
 * @returns {string} — "normal", "high", "low" или "neutral"
 */
function classifyValue(value, low, high) {
  var num = parseFloat(value);
  if (isNaN(num)) return 'neutral';
  if (num < low) return 'low';
  if (num > high) return 'high';
  return 'normal';
}
